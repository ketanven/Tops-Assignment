//Q-14. Find ascii value of given number

#include <stdio.h>

int main() {
    int num; // Declare a variable to store an integer
    
    printf("Enter number :");
    
    // Read the number entered by the user
    scanf("%d",&num);
    
    // Print the character corresponding to the ASCII value entered
    printf("\nascii value of number is : %c", num);
}


